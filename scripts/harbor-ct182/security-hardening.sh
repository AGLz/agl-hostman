# 9. Backup Encryption
configure_backup_encryption() {
    echo -e "\n${BLUE}═══ Configuring Encrypted Backups ═══${NC}\n"

    pct exec "$CT_ID" -- bash -c "
        set -e

        # Install encryption tools
        apt-get update
        apt-get install -y gpg

        # Create encrypted backup script
        cat > /usr/local/bin/harbor-backup-encrypted.sh << 'EOFBACKUP'
#!/bin/bash
# Harbor Encrypted Backup Script

BACKUP_DIR=\"${DATA_VOLUME}/backups\"
BACKUP_DATE=\$(date +%Y%m%d)
BACKUP_PATH=\"\$BACKUP_DIR/harbor-backup-\$BACKUP_DATE\"
GPG_RECIPIENT=\"harbor-backup@example.com\"

mkdir -p \"\$BACKUP_PATH\"

# Backup PostgreSQL database
docker exec harbor-db pg_dumpall -U postgres > \"\$BACKUP_PATH/harbor-db.sql\"

# Backup configuration
cp -r /root/harbor/harbor.yml \"\$BACKUP_PATH/\"
cp -r /root/harbor/common/config \"\$BACKUP_PATH/\"

# Backup certificates
cp -r ${DATA_VOLUME}/secrets \"\$BACKUP_PATH/\"

# Create encrypted tarball
tar czf - -C \"\$BACKUP_DIR\" \"harbor-backup-\$BACKUP_DATE\" | \\
    gpg --encrypt --recipient \"\$GPG_RECIPIENT\" > \"\$BACKUP_PATH.tar.gz.gpg\"

# Remove unencrypted backup
rm -rf \"\$BACKUP_PATH\"

# Cleanup old backups (30 days)
find \"\$BACKUP_DIR\" -name \"harbor-backup-*.tar.gz.gpg\" -mtime +30 -delete

echo \"\$(date): Encrypted backup completed\" >> /var/log/harbor-backup.log
EOFBACKUP

        chmod +x /usr/local/bin/harbor-backup-encrypted.sh
    "

    warn "GPG encryption requires key setup - configure GPG recipient before use"
    success "Encrypted backup script created"
}

