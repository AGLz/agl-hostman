# 1. Firewall Configuration
configure_firewall() {
    echo -e "\n${BLUE}═══ Configuring Firewall Rules ═══${NC}\n"

    pct exec "$CT_ID" -- bash <<'EOF'
        set -e

        # Install iptables-persistent
        export DEBIAN_FRONTEND=noninteractive
        apt-get update
        apt-get install -y iptables-persistent

        # Flush existing rules
        iptables -F
        iptables -X

        # Default policies
        iptables -P INPUT DROP
        iptables -P FORWARD DROP
        iptables -P OUTPUT ACCEPT

        # Allow loopback
        iptables -A INPUT -i lo -j ACCEPT
        iptables -A OUTPUT -o lo -j ACCEPT

        # Allow established connections
        iptables -A INPUT -m conntrack --ctstate ESTABLISHED,RELATED -j ACCEPT

        # Allow SSH (port 22)
        iptables -A INPUT -p tcp --dport 22 -j ACCEPT

        # Allow HTTP (port 80) - redirect to HTTPS
        iptables -A INPUT -p tcp --dport 80 -j ACCEPT

        # Allow HTTPS (port 443)
        iptables -A INPUT -p tcp --dport 443 -j ACCEPT

        # Allow Docker Content Trust/Notary (port 4443) - if enabled
        # iptables -A INPUT -p tcp --dport 4443 -j ACCEPT

        # Allow ping (ICMP)
        iptables -A INPUT -p icmp --icmp-type echo-request -j ACCEPT

        # Log dropped packets (debugging)
        iptables -A INPUT -j LOG --log-prefix "IPTables-Dropped: " --log-level 4

        # Save rules
        netfilter-persistent save

        # Verify rules
        iptables -L -n -v
EOF

    success "Firewall configured with restrictive rules"
}

# 2. SSL/TLS Security
harden_ssl_tls() {
    echo -e "\n${BLUE}═══ Hardening SSL/TLS Configuration ═══${NC}\n"

    pct exec "$CT_ID" -- bash -c "
        set -e
        cd /root/harbor

        # Create custom Nginx SSL configuration
        cat > /root/harbor/common/config/nginx/ssl.conf << 'EOFNGINX'
# Strong SSL/TLS Configuration for Harbor
# Generated by Harbor CT182 Security Hardening Script

# Protocols - TLS 1.2 and 1.3 only
ssl_protocols TLSv1.2 TLSv1.3;

# Strong cipher suites
ssl_ciphers 'ECDHE-ECDSA-AES128-GCM-SHA256:ECDHE-RSA-AES128-GCM-SHA256:ECDHE-ECDSA-AES256-GCM-SHA384:ECDHE-RSA-AES256-GCM-SHA384:ECDHE-ECDSA-CHACHA20-POLY1305:ECDHE-RSA-CHACHA20-POLY1305:DHE-RSA-AES128-GCM-SHA256:DHE-RSA-AES256-GCM-SHA384';

# Server cipher preference
ssl_prefer_server_ciphers on;

# SSL session cache
ssl_session_cache shared:SSL:50m;
ssl_session_timeout 1d;

# SSL session tickets disabled (for perfect forward secrecy)
ssl_session_tickets off;

# OCSP stapling
ssl_stapling on;
ssl_stapling_verify on;

# HSTS (HTTP Strict Transport Security)
add_header Strict-Transport-Security \"max-age=63072000; includeSubDomains; preload\" always;

# Security headers
add_header X-Frame-Options \"SAMEORIGIN\" always;
add_header X-Content-Type-Options \"nosniff\" always;
add_header X-XSS-Protection \"1; mode=block\" always;
add_header Referrer-Policy \"no-referrer-when-downgrade\" always;
add_header Content-Security-Policy \"default-src 'self' 'unsafe-inline' 'unsafe-eval'; img-src 'self' data: https:; font-src 'self' data:; connect-src 'self' https:; frame-ancestors 'self';\" always;

# Disable server tokens (hide Nginx version)
server_tokens off;
EOFNGINX

        # Restart Nginx container to apply changes
        docker compose restart nginx
    "

    success "SSL/TLS security hardened"
}

