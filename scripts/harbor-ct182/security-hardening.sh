    pct exec "$CT_ID" -- bash <<'EOF'
        set -e

        # Disable unnecessary services
        systemctl disable bluetooth || true
        systemctl disable cups || true

        # Configure sysctl security parameters
        cat >> /etc/sysctl.conf << 'EOFSYSCTL'

