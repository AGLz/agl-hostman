    pct exec "$CT_ID" -- bash -c "
        set -e

        # Secure Harbor configuration files
        chmod 600 /root/harbor/harbor.yml
        chown root:root /root/harbor/harbor.yml

        # Secure SSL certificates
        chmod 600 ${DATA_VOLUME}/secrets/cert/server.key
        chmod 644 ${DATA_VOLUME}/secrets/cert/server.crt
        chown -R 10000:10000 ${DATA_VOLUME}/secrets

        # Secure data directories
        chmod 755 ${DATA_VOLUME}
        chmod 700 ${DATA_VOLUME}/database
        chmod 700 ${DATA_VOLUME}/secrets

        # Secure log files
        chmod 755 /var/log/harbor

        # Remove world-readable permissions
        find ${DATA_VOLUME} -type f -perm /o+r -exec chmod o-r {} \;
        find ${DATA_VOLUME} -type d -perm /o+x -exec chmod o-x {} \;
    "

    success "File system permissions secured"
}

