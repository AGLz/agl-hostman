# Generate GitLab CI Configuration
generate_gitlab_ci() {
    echo -e "\n${BLUE}═══ Generating GitLab CI Configuration ═══${NC}\n"

    cat > ".gitlab-ci.yml" << 'EOF'
# GitLab CI/CD Configuration for Harbor CT182
# Generated by Harbor CT182 CI/CD Integration Script

variables:
  HARBOR_REGISTRY: harbor.yourdomain.com
  HARBOR_PROJECT: library
  IMAGE_NAME: $HARBOR_REGISTRY/$HARBOR_PROJECT/$CI_PROJECT_NAME
  IMAGE_TAG: $CI_COMMIT_SHORT_SHA

stages:
  - build
  - scan
  - push
  - deploy

# Build Docker Image
build:
  stage: build
  image: docker:latest
  services:
    - docker:dind
  script:
    - docker build -t $IMAGE_NAME:$IMAGE_TAG .
    - docker tag $IMAGE_NAME:$IMAGE_TAG $IMAGE_NAME:latest
    - docker save $IMAGE_NAME:$IMAGE_TAG > image.tar
  artifacts:
    paths:
      - image.tar
    expire_in: 1 hour

# Security Scan
security_scan:
  stage: scan
  image: aquasec/trivy:latest
  script:
    - docker load < image.tar
    - trivy image --severity HIGH,CRITICAL --exit-code 1 $IMAGE_NAME:$IMAGE_TAG
  dependencies:
    - build
  allow_failure: false

# Push to Harbor
push:
  stage: push
  image: docker:latest
  services:
    - docker:dind
  before_script:
    - docker login $HARBOR_REGISTRY -u $HARBOR_ROBOT_USER -p $HARBOR_ROBOT_PASSWORD
  script:
    - docker load < image.tar
    - docker push $IMAGE_NAME:$IMAGE_TAG
    - docker push $IMAGE_NAME:latest
  dependencies:
    - build
    - security_scan
  only:
    - main
    - develop

# Deploy (Example)
deploy:
  stage: deploy
  image: bitnami/kubectl:latest
  script:
    - kubectl set image deployment/myapp myapp=$IMAGE_NAME:$IMAGE_TAG
  only:
    - main
  when: manual

# Required CI/CD Variables (Settings → CI/CD → Variables):
# - HARBOR_ROBOT_USER: robot$gitlab-ci
# - HARBOR_ROBOT_PASSWORD: [robot account password]
EOF

    success "GitLab CI configuration generated: .gitlab-ci.yml"

    info "Required GitLab CI/CD variables:"
    echo "  HARBOR_ROBOT_USER = robot\$gitlab-ci"
    echo "  HARBOR_ROBOT_PASSWORD = [robot account password]"
}

# Generate GitHub Actions Configuration
generate_github_actions() {
    echo -e "\n${BLUE}═══ Generating GitHub Actions Configuration ═══${NC}\n"

    mkdir -p ".github/workflows"

    cat > ".github/workflows/harbor-deploy.yml" << 'EOF'
# GitHub Actions Workflow for Harbor CT182
# Generated by Harbor CT182 CI/CD Integration Script

name: Build and Push to Harbor

on:
  push:
    branches:
      - main
      - develop
  pull_request:
    branches:
      - main

env:
  HARBOR_REGISTRY: harbor.yourdomain.com
  HARBOR_PROJECT: library
  IMAGE_NAME: ${{ github.repository }}

jobs:
  build-and-push:
    runs-on: ubuntu-latest

    steps:
      - name: Checkout code
        uses: actions/checkout@v3

      - name: Set up Docker Buildx
        uses: docker/setup-buildx-action@v2

      - name: Log in to Harbor
        uses: docker/login-action@v2
        with:
          registry: ${{ env.HARBOR_REGISTRY }}
          username: ${{ secrets.HARBOR_ROBOT_USER }}
          password: ${{ secrets.HARBOR_ROBOT_PASSWORD }}

      - name: Extract metadata
        id: meta
        uses: docker/metadata-action@v4
        with:
          images: ${{ env.HARBOR_REGISTRY }}/${{ env.HARBOR_PROJECT }}/${{ env.IMAGE_NAME }}
          tags: |
            type=ref,event=branch
            type=ref,event=pr
            type=sha,prefix={{branch}}-
            type=raw,value=latest,enable={{is_default_branch}}

      - name: Build and push image
        uses: docker/build-push-action@v4
        with:
          context: .
          push: ${{ github.event_name != 'pull_request' }}
          tags: ${{ steps.meta.outputs.tags }}
          labels: ${{ steps.meta.outputs.labels }}
          cache-from: type=gha
          cache-to: type=gha,mode=max

      - name: Run Trivy vulnerability scanner
        uses: aquasecurity/trivy-action@master
        with:
          image-ref: ${{ env.HARBOR_REGISTRY }}/${{ env.HARBOR_PROJECT }}/${{ env.IMAGE_NAME }}:${{ github.sha }}
          format: 'sarif'
          output: 'trivy-results.sarif'
          severity: 'CRITICAL,HIGH'

      - name: Upload Trivy results to GitHub Security
        uses: github/codeql-action/upload-sarif@v2
        if: always()
        with:
          sarif_file: 'trivy-results.sarif'

# Required GitHub Secrets (Settings → Secrets → Actions):
# - HARBOR_ROBOT_USER: robot$github-actions
# - HARBOR_ROBOT_PASSWORD: [robot account password]
EOF

    success "GitHub Actions workflow generated: .github/workflows/harbor-deploy.yml"

    info "Required GitHub Secrets:"
    echo "  HARBOR_ROBOT_USER = robot\$github-actions"
    echo "  HARBOR_ROBOT_PASSWORD = [robot account password]"
}

# Generate Jenkins Pipeline
generate_jenkins_pipeline() {
    echo -e "\n${BLUE}═══ Generating Jenkins Pipeline Configuration ═══${NC}\n"

    cat > "Jenkinsfile" << 'EOF'
// Jenkins Pipeline for Harbor CT182
// Generated by Harbor CT182 CI/CD Integration Script

pipeline {
    agent any

    environment {
        HARBOR_REGISTRY = 'harbor.yourdomain.com'
        HARBOR_PROJECT = 'library'
        HARBOR_CREDENTIALS = credentials('harbor-robot-credentials')
        IMAGE_NAME = "${HARBOR_REGISTRY}/${HARBOR_PROJECT}/${JOB_NAME}"
        IMAGE_TAG = "${BUILD_NUMBER}"
    }

    stages {
        stage('Checkout') {
            steps {
                checkout scm
            }
        }

        stage('Build') {
            steps {
                script {
                    docker.build("${IMAGE_NAME}:${IMAGE_TAG}")
                    docker.build("${IMAGE_NAME}:latest")
                }
            }
        }

        stage('Security Scan') {
            steps {
                script {
                    sh """
                        docker run --rm \
                            -v /var/run/docker.sock:/var/run/docker.sock \
                            aquasec/trivy:latest image \
                            --severity HIGH,CRITICAL \
                            --exit-code 1 \
                            ${IMAGE_NAME}:${IMAGE_TAG}
                    """
                }
            }
        }

        stage('Push to Harbor') {
            steps {
                script {
                    docker.withRegistry("https://${HARBOR_REGISTRY}", 'harbor-robot-credentials') {
                        docker.image("${IMAGE_NAME}:${IMAGE_TAG}").push()
                        docker.image("${IMAGE_NAME}:latest").push()
                    }
                }
            }
        }

        stage('Deploy') {
            when {
                branch 'main'
            }
            steps {
                script {
                    // Add deployment steps here
                    echo "Deploying ${IMAGE_NAME}:${IMAGE_TAG}"
                }
            }
        }
    }

    post {
        always {
            cleanWs()
        }
        success {
            echo "Pipeline completed successfully"
        }
        failure {
            echo "Pipeline failed"
        }
    }
}

/* Required Jenkins Credentials:
 * ID: harbor-robot-credentials
 * Type: Username with password
 * Username: robot$jenkins
 * Password: [robot account password]
 */
EOF

    success "Jenkins pipeline generated: Jenkinsfile"

    info "Required Jenkins credentials:"
    echo "  ID: harbor-robot-credentials"
    echo "  Type: Username with password"
    echo "  Username: robot\$jenkins"
    echo "  Password: [robot account password]"
}

