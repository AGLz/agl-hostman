# Harbor Webhook Configuration

Harbor supports webhooks for various events to integrate with external systems.

## Supported Events

- **PUSH_ARTIFACT**: Image pushed to repository
- **PULL_ARTIFACT**: Image pulled from repository
- **DELETE_ARTIFACT**: Image deleted
- **SCANNING_COMPLETED**: Vulnerability scan completed
- **SCANNING_FAILED**: Vulnerability scan failed
- **QUOTA_EXCEED**: Storage quota exceeded
- **REPLICATION_SUCCESS**: Replication completed successfully
- **REPLICATION_FAILURE**: Replication failed

## Configuration Steps

1. Navigate to Harbor UI
2. Go to Projects → [Your Project] → Webhooks
3. Click "New Webhook"
4. Configure:
   - Name: descriptive name
   - Notify Type: http, Slack, or other supported types
   - Event Types: select events to monitor
   - Endpoint URL: your webhook receiver URL

## Example Webhook Payload

```json
{
  "type": "PUSH_ARTIFACT",
  "occur_at": 1234567890,
  "operator": "admin",
  "event_data": {
    "resources": [{
      "resource_url": "harbor.yourdomain.com/library/myapp:v1.0"
    }],
    "repository": {
      "name": "library/myapp"
    }
  }
}
```

## Slack Integration Example

```
Webhook URL: https://hooks.slack.com/services/YOUR/SLACK/WEBHOOK
