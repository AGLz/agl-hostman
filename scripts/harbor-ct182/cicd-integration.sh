```python
from flask import Flask, request
app = Flask(__name__)

@app.route('/harbor-webhook', methods=['POST'])
def harbor_webhook():
    payload = request.json
    event_type = payload.get('type')

    if event_type == 'PUSH_ARTIFACT':
        # Handle image push event
        print(f"Image pushed: {payload['event_data']['resources'][0]['resource_url']}")

    return '', 200

if __name__ == '__main__':
    app.run(host='0.0.0.0', port=5000)
```
EOF

